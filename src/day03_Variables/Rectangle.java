package day03_Variables;

public class Rectangle {
    public static void main(String[] args) {

        int w = 4;
        int l = 5;
        int area = w * l;
        int perimeter = 2 * l + 2 * w;

        System.out.println("Area = " + area);
        System.out.println("Perimeter = " + perimeter);
    }
}
