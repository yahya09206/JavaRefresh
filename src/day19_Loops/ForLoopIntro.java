package day19_Loops;

public class ForLoopIntro {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("------------------------------------");

        for (int i = 1; i <= 5; i++){
            System.out.println("Hello World");
        }
    }
}
